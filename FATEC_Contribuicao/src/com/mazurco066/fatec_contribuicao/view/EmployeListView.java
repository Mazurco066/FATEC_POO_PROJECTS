    private void feedTable(){
        
        DefaultTableModel modelo = (DefaultTableModel) Tabela.getModel();
        modelo.setNumRows(0);
        
        Empregados.stream().forEach((Employe _item) -> {
            modelo.addRow(new Object[] {
                _item.getID(),
                _item.getNome(),
                _item.getSalario(),
                _item.getRecinss()
            });
        });
    }

